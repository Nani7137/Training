package com.sonata;

public class DataTypes {
	public static void main(String[] args) {
		int a = 10;
		float b = 10.2f;
		double c = 200.0;
		long d = 3000l;
		byte e = 2;
		short f = 5;
		char g = 'i';
		String h = "Hello Hai";
		boolean i = true;

		System.out.println("int value is : " + a);
		System.out.println("float value is : " +b);
		System.out.println("double value is : " + c);
		System.out.println("long value is : " + d);
		System.out.println("byte value is : " + e);
		System.out.println("short value is : "+ f);
		System.out.println("char value is : "+ g);
		System.out.println("string value is : "+ h);
		System.out.println("boolean value is : "+ i);
	}
			
}
